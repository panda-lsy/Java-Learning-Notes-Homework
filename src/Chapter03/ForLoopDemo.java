package Chapter03;

public class ForLoopDemo {
    public static void main(String[] args) {
        // Print numbers from 1 to 10 using a for loop
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        int j=11;
        while(j<=20){
            System.out.println(j);
            j++;
        }
    }
}
